	 public double getNumFromTextField(JTextField c, double defVal) {
			if (!c.isEnabled())
			    return -1;
			String nstr = c.getText();
			Double n=new Double(defVal);
			try {
			    n = new Double(nstr);
			} 
			catch (Exception e) { 
			    System.out.println("error in getNumFromTextField, nstr="+nstr+"  , exception=" + e);
			    return -1; /*err*/
			}
			return n.doubleValue();
		    }
	
	 
